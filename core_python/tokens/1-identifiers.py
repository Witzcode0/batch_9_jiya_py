# In Python, an identifier is a name used to identify a variable, function, class, module, or any other object in a program. These are user-defined names that help in uniquely identifying entities and make the code readable and organized. 


# Allowed Characters: Identifiers can be a combination of lowercase letters (a-z), uppercase letters (A-Z), digits (0-9), and underscores (_).

# Cannot Start with a Digit: An identifier cannot begin with a number (e.g., 1variable is invalid, but variable1 is valid).

# Case-Sensitive: Python is a case-sensitive language. Therefore, name and Name are considered two different identifiers.

# No Special Characters or Spaces: Identifiers cannot contain special characters like !, @, #, $, % or spaces.
# first name

# Avoid Keywords: You cannot use any of Python's reserved keywords (e.g., if, for, class, True, False, None) as an identifier.

# Length: There is no hard limit on the length of an identifier, but it's best practice to keep names meaningful and reasonably short (generally within 79 characters as per PEP 8 guidelines for line length). 

# Examples
# Valid Identifiers 	Invalid Identifiers	    Reason for Invalidity
# user_name	            user@name	            Contains a special character @
# total_score	        3rd_place	            Starts with a digit
# calculate_area	    for	                    for is a reserved keyword
# Student	            student name	         Contains a space


# Variables, functions, and methods: Use lowercase letters with words separated by underscores (snake_case).
# first_name, area_of_circle

# Classes: Use the PascalCase convention (capitalize the first letter of each word).
# FirstName, AreaOfCircle

# camelCase
# firstName, areaOfCircle

# Constants: Use uppercase letters with words separated by underscores (UPPERCASE_WITH_UNDERSCORES).
# PI = 3.14

# Descriptive Names: Choose names that clearly convey the purpose of the entity (e.g., count is better than c). 